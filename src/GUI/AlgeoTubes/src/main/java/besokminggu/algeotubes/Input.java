    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        AlgeoTubes.n = getN();
        AlgeoTubes.m = getM();
        if (AlgeoTubes.n <= 0 || AlgeoTubes.m<=0) {
            JOptionPane.showMessageDialog(null, "Uh-oh! Inputnya error mas/mba! Error code = 2", "Error Ngabs Besar Array", JOptionPane.ERROR_MESSAGE);
        } else if (AlgeoTubes.n != AlgeoTubes.m && (AlgeoTubes.DETDeterminanNxN == 1 || AlgeoTubes.DETDeterminandenganKofaktor == 1 || AlgeoTubes.DETDeterminanMatriksSegitiga == 1 || AlgeoTubes.INVAdjoin == 1 || AlgeoTubes.INVBalikan == 1)){
            JOptionPane.showMessageDialog(null, "Uh-oh! Inputnya error mas/mba! Error code = 3", "Error Ngabs Kayaknya Harus NxN", JOptionPane.ERROR_MESSAGE);
        } else if (AlgeoTubes.n > 3 && AlgeoTubes.m > 3  && AlgeoTubes.DETDeterminanNxN == 1) {
            JOptionPane.showMessageDialog(null, "Uh-oh! Inputnya error mas/mba! Error code = 4", "Error Ngabs Kayaknya Harus Kurang dari 3 lebih dari 0", JOptionPane.ERROR_MESSAGE);
        } else {
            close();
            if(AlgeoTubes.InterpolasiPolinom == 1 || AlgeoTubes.InterpolasiBicubicSpline == 1 || AlgeoTubes.ImplementasiInterpolasiBicubicSpline == 1){
                new SpecialInput().setVisible(true);
            }
            new InputMatriksKeyboard().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

